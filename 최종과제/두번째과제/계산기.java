package 두번째과제;

public class 계산기 {
	public void add (double num1, double num2){
		System.out.println("두 수의 합은 : " + (num1 + num2) + "입니다. \n");
	}
	
	public void sub (double num1, double num2){
		System.out.println("두 수의 차는 : " + (num1 - num2) + "입니다. \n");
	}
	
	public void mul (double num1, double num2){
		System.out.println("두 수의 곱은 : " + (num1 * num2) + "입니다. \n");
	}
	
	public void div (double num1, double num2){
		System.out.println("두 수를 나눈 몫은 : " + (num1 / num2) + "입니다. \n");
	}
}